def update_footer(file_path):
    with open(file_path, 'r', encoding='utf-8') as f:
        content = f.read()
    
    # Find footer-top and its ul
    pattern = r'(<div class="footer-top">\s*<ul class="footer-links">.*?</ul>\s*)(</div>)'
    new_content = re.sub(pattern, r'\1' + sns_html + r'        \2', content, flags=re.DOTALL)
    
    if new_content != content:
        with open(file_path, 'w', encoding='utf-8') as f:
            f.write(new_content)
        print(f"Updated {file_path}")

for filename in os.listdir('.'):
    if filename.endswith('.html'):
        update_footer(filename)
